package ex04;

// 계산기 클래스 = 설계도
public class Calculator { // 메모리에는 클래스안의 메소드 add가 뜨게 되는 것이다.

        public int add(int n1, int n2) {
            return n1 + n2;
        }
        public int mul(int n1, int n2) {
            return n1 * n2;
        }
        public int minus(int n1, int n2) {
            return n1 - n2;
        }
        public int divide(int n1 , int n2) {
            return n1 / n2;
        }

    }

